//Increments

#include <stdio.h>
int main()
{
int pog = 20;
printf("%d\n", pog);

pog = pog + 1;
printf("%d\n", pog);

int doge = 68;
printf("%d\n", doge);

doge++;
printf("%d\n", doge);

int snoop = 421;
printf("%d\n", snoop);

snoop--;
printf("%d\n", snoop);

int alpha = 50, beta = 0;
printf("%d\n", alpha);

beta = --alpha;

printf("%d\n", beta);


return 0;
}
